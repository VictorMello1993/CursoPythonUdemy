#----------------------------------------------------------------------------------------------------------------------------------------
#Sem utilizar recursos de packing e unpacking - método tradicional
def soma_3(a, b, c):
    return a + b + c


print(soma_3(1, 2, 3))
#----------------------------------------------------------------------------------------------------------------------------------------

#----------------------------------------------------------------------------------------------------------------------------------------
# Packing (empacotamento para uma tupla)
def soma_n(*nums):
    soma = 0
    for n in nums:
        soma += n
    return soma

print(soma_n(1, 2, 3, 4, 5))
#----------------------------------------------------------------------------------------------------------------------------------------
#Unpacking (desempacotamento - Desmembrando uma coleção de dados qualquer)
t_nums = (1, 2, 3)
print(soma_3(*t_nums))

lista_nums = [1, 2, 3]
print(soma_3(*lista_nums)) #Desempacotando uma lista
#----------------------------------------------------------------------------------------------------------------------------------------